def _add_summary_slide(prs, title: str, content: str, colors):
    slide, left, _ = _blank_slide(prs, colors)

    # Left colored panel
    panel = slide.shapes.add_shape(1, Inches(0), Inches(0), Inches(4.5), Inches(7.5))
    panel.fill.solid()
    panel.fill.fore_color.rgb = RGBColor(*colors["primary"])
    panel.line.fill.background()

    # Panel title
    txBox = slide.shapes.add_textbox(Inches(1), Inches(2), Inches(3), Inches(3))
    tf = txBox.text_frame
    tf.word_wrap = True
    p = tf.paragraphs[0]
    p.text = title
    p.font.size = Pt(32)
    p.font.bold = True
    p.font.color.rgb = RGBColor(*colors["white"])

    # Right side bullets
    items = [b.strip() for b in content.replace(";", "\n").split("\n") if b.strip()]
    top = Inches(1.2)
    for item in items:
        parts = item.split("|", 1)
        txBox2 = slide.shapes.add_textbox(Inches(5.2), top, Inches(7), Inches(0.8))
        tf2 = txBox2.text_frame
        tf2.word_wrap = True
        p2 = tf2.paragraphs[0]
        if len(parts) == 2:
            run = p2.add_run()
            run.text = f"{parts[0].strip()}  "
            run.font.size = Pt(18)
            run.font.bold = True
            run.font.color.rgb = RGBColor(*colors["primary"])
            run2 = p2.add_run()
            run2.text = parts[1].strip()
            run2.font.size = Pt(14)
            run2.font.color.rgb = RGBColor(*colors["text"])
        else:
            p2.text = item
            p2.font.size = Pt(16)
            p2.font.color.rgb = RGBColor(*colors["text"])
        top += Inches(1.1)


