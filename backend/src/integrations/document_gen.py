async def create_pptx(
    title: str,
    slides: str,
    theme: str = "blue",
    transition: str = "fade",
    filename: str = "presentation.pptx",
    session=None,
    workspace_id: str = "default",
) -> str:
    """Generate a professional animated PowerPoint deck.

    Args:
        title: Presentation title (slide 1 heading)
        theme: Color theme — blue, dark, green, warm
        transition: Slide transition — fade, push, wipe, cover, uncover, dissolve, none
        slides: Full slide deck spec as JSON array. Each slide object:
          {
            "type": "title|section|content|two_column|big_number|quote|summary|thank_you",
            "title": "Slide heading",
            "content": "Text | bullet1; bullet2",      # for content, two_column
            "content_right": "Right column text",       # for two_column
            "number": "85%",                            # for big_number
            "number_label": "Customer satisfaction",    # for big_number
            "quote": "Inspirational text",              # for quote
            "quote_author": "Author name"               # for quote
          }

    Example slides JSON:
    [
      {"type":"title","title":"Q4 Strategy","content":"Product Division | Jan 2026"},
      {"type":"big_number","number":"2.4x","number_label":"Revenue growth YoY"},
      {"type":"section","title":"Market Analysis"},
      {"type":"content","title":"Key Trends","content":"Trend 1 | AI adoption accelerating; Mobile first; Regulatory shifts"},
      {"type":"two_column","title":"Competitive Landscape","content":"Strengths | Fast execution; Strong brand","content_right":"Weaknesses | Legacy infra; Talent gap"},
      {"type":"quote","quote":"The best way to predict the future is to invent it.","quote_author":"Alan Kay"},
      {"type":"summary","title":"Next Steps","content":"Q1 focus | Launch APAC region; Hire 3 senior engineers; Close Series B"},
      {"type":"thank_you","title":"Thank You","content":"questions@company.com"}
    ]
    """
    colors = THEMES.get(theme, THEMES["blue"])
    trans = transition if transition in TRANSITIONS else "fade"
    Presentation, Inches, Pt, RGBColor, PP_ALIGN = _get_pptx()
    prs = Presentation()
    prs.slide_width = Inches(13.333)
    prs.slide_height = Inches(7.5)

    # Parse slides JSON
    try:
        slide_specs = json.loads(slides)
    except (json.JSONDecodeError, TypeError):
        # Fallback: treat as newline-delimited format
        slide_specs = _parse_legacy_slides(slides)

    for idx, spec in enumerate(slide_specs):
        slide_type = spec.get("type", "content")
        slide_title = spec.get("title", "")
        slide_content = spec.get("content", "")

        if slide_type == "title":
            _add_title_slide(prs, slide_title, slide_content, colors)
        elif slide_type == "section":
            _add_section_slide(prs, slide_title, slide_content, colors)
        elif slide_type == "big_number":
            _add_big_number_slide(prs, slide_title, spec.get("number", ""), spec.get("number_label", ""), colors)
        elif slide_type == "quote":
            _add_quote_slide(prs, spec.get("quote", ""), spec.get("quote_author", ""), colors)
        elif slide_type == "two_column":
            _add_two_column_slide(prs, slide_title, slide_content, spec.get("content_right", ""), colors)
        elif slide_type == "summary":
            _add_summary_slide(prs, slide_title, slide_content, colors)
        elif slide_type == "thank_you":
            _add_thank_you_slide(prs, slide_title, slide_content, colors)
        else:
            # Default: content slide
            _add_content_slide(prs, slide_title, slide_content, colors)

        # ── Apply transition to the slide we just added ──────
        if trans != "none" and len(prs.slides) > 0:
            _set_slide_transition(prs.slides[-1], trans)

    safe_name = _safe_name(filename, ".pptx")
    out_path = _outputs_dir() / safe_name
    prs.save(str(out_path))

    return json.dumps({
        "ok": True,
        "filename": safe_name,
        "path": str(out_path),
        "slides": len(prs.slides),
        "size_bytes": out_path.stat().st_size,
        "format": "pptx",
        "theme": theme,
        "transition": trans,
    }, indent=2)


# ── Slide builders ──────────────────────────────────────────────

def _blank_slide(prs, colors) -> tuple:
    layout = prs.slide_layouts[6]  # blank
    slide = prs.slides.add_slide(layout)
    bg = slide.background
    fill = bg.fill
    fill.solid()
    fill.fore_color.rgb = RGBColor(*colors["white"])
    return slide, Inches(0.8), Inches(0)


def _set_slide_transition(slide, transition_type: str):
    """Inject slide transition via XML."""
    nsmap = {"p": "http://schemas.openxmlformats.org/presentationml/2006/main"}
    from lxml import etree
    try:
        trans_elem = etree.SubElement(
            slide.element,
            "{http://schemas.openxmlformats.org/presentationml/2006/main}transition",
        )
        speed = "slow" if transition_type == "dissolve" else "fast"
        attrs = {"spd": speed}
        if transition_type == "fade":
            etree.SubElement(trans_elem, "{http://schemas.openxmlformats.org/presentationml/2006/main}fade")
        elif transition_type in ("push", "wipe", "cover", "uncover"):
            child = etree.SubElement(trans_elem, f"{{http://schemas.openxmlformats.org/presentationml/2006/main}}{transition_type}")
            child.set("dir", "d")  # direction: down
        elif transition_type == "dissolve":
            etree.SubElement(trans_elem, "{http://schemas.openxmlformats.org/presentationml/2006/main}dissolve")
    except Exception:
        pass  # Non-critical — slide just won't have transition


def _add_entrance_animation(shape, effect: str = "fade"):
    """Add entrance animation to a shape via XML."""
    from lxml import etree
    try:
        nsmap = {
            "p": "http://schemas.openxmlformats.org/presentationml/2006/main",
            "a": "http://schemas.openxmlformats.org/drawingml/2006/main",
        }
        # Build animation timing
        tree = shape._element
        # Find or create the timing element
        sp_tree_elem = tree.getparent()
        if sp_tree_elem is None:
            return
        # Simple fade-in effect
        c_bhvr = etree.SubElement(sp_tree_elem, "{http://schemas.openxmlformats.org/presentationml/2006/main}cBhvr")
        c_bhvr.set("rctx", shape.shape_id)
        # This is simplified — full animation XML is complex
    except Exception:
        pass  # Animation is non-critical


def _add_title_slide(prs, title: str, subtitle: str, colors):
    slide, left, _ = _blank_slide(prs, colors)
    # Dark background
    bg = slide.background
    fill = bg.fill
    fill.solid()
    fill.fore_color.rgb = RGBColor(*colors["primary"])

    # Accent bar
    bar = slide.shapes.add_shape(1, Inches(1), Inches(3.2), Inches(2), Inches(0.06))
    bar.fill.solid()
    bar.fill.fore_color.rgb = RGBColor(*colors["accent"])
    bar.line.fill.background()

    # Title
    txBox = slide.shapes.add_textbox(Inches(1), Inches(1.8), Inches(11.333), Inches(1.5))
    tf = txBox.text_frame
    tf.word_wrap = True
    p = tf.paragraphs[0]
    p.text = title
    p.font.size = Pt(42)
    p.font.bold = True
    p.font.color.rgb = RGBColor(*colors["white"])

    # Subtitle
    if subtitle:
        txBox2 = slide.shapes.add_textbox(Inches(1), Inches(3.6), Inches(11.333), Inches(1))
        tf2 = txBox2.text_frame
        p2 = tf2.paragraphs[0]
        p2.text = subtitle
        p2.font.size = Pt(20)
        p2.font.color.rgb = RGBColor(*colors["accent"])


