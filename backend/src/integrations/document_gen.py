# ── Slide helpers ───────────────────────────────────────────────

def _slide_heading(slide, title: str, colors):
    txBox = slide.shapes.add_textbox(Inches(0.8), Inches(0.4), Inches(11.733), Inches(0.9))
    tf = txBox.text_frame
    tf.word_wrap = True
    p = tf.paragraphs[0]
    p.text = title
    p.font.size = Pt(28)
    p.font.bold = True
    p.font.color.rgb = RGBColor(*colors["primary"])


def _accent_line(slide, colors):
    line = slide.shapes.add_shape(1, Inches(0.8), Inches(1.25), Inches(2), Inches(0.04))
    line.fill.solid()
    line.fill.fore_color.rgb = RGBColor(*colors["accent"])
    line.line.fill.background()


def _bullet_list(slide, items: list[str], top_start, colors):
    top = top_start
    for item in items:
        parts = item.split("|", 1)
        txBox = slide.shapes.add_textbox(Inches(0.8), top, Inches(11.733), Inches(0.9))
        tf = txBox.text_frame
        tf.word_wrap = True
        p = tf.paragraphs[0]
        if len(parts) == 2:
            run = p.add_run()
            run.text = f"  {parts[0].strip()}"
            run.font.size = Pt(18)
            run.font.bold = True
            run.font.color.rgb = RGBColor(*colors["primary"])
            p2 = tf.add_paragraph()
            run2 = p2.add_run()
            run2.text = f"     {parts[1].strip()}"
            run2.font.size = Pt(14)
            run2.font.color.rgb = RGBColor(*colors["text"])
            p2.space_after = Pt(6)
        else:
            p.text = f"  {item}"
            p.font.size = Pt(18)
            p.font.color.rgb = RGBColor(*colors["text"])
        p.space_after = Pt(4)
        top += Inches(0.85)


def _safe_name(filename: str, default_ext: str) -> str:
    safe = Path(filename).name.replace("..", "").replace("/", "_")
    if not safe.endswith(default_ext):
        safe = safe.rsplit(".", 1)[0] + default_ext if "." in safe else safe + default_ext
    return safe


def _parse_legacy_slides(text: str) -> list[dict]:
    """Parse old line-based format: 'Title | bullet1; bullet2'."""
    specs = []
    for line in text.strip().split("\n"):
        line = line.strip()
        if not line:
            continue
        parts = line.split("|", 1)
        specs.append({
            "type": "content",
            "title": parts[0].strip(),
            "content": parts[1].strip() if len(parts) > 1 else "",
        })
    return specs
