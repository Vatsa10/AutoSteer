def _add_content_slide(prs, title: str, content: str, colors):
    slide, left, _ = _blank_slide(prs, colors)

    # Title
    _slide_heading(slide, title, colors)

    # Accent line
    _accent_line(slide, colors)

    # Bullets
    bullets = [b.strip() for b in content.replace(";", "\n").split("\n") if b.strip()]
    _bullet_list(slide, bullets, Inches(1.7), colors)


def _add_two_column_slide(prs, title: str, left_content: str, right_content: str, colors):
    slide, left, _ = _blank_slide(prs, colors)
    _slide_heading(slide, title, colors)
    _accent_line(slide, colors)

    # Left column
    left_items = [b.strip() for b in left_content.replace(";", "\n").split("\n") if b.strip()]
    txBox = slide.shapes.add_textbox(Inches(0.8), Inches(1.8), Inches(5.5), Inches(5))
    tf = txBox.text_frame
    tf.word_wrap = True
    for i, item in enumerate(left_items):
        p = tf.paragraphs[0] if i == 0 else tf.add_paragraph()
        parts = item.split("|", 1)
        if len(parts) == 2:
            run = p.add_run()
            run.text = parts[0].strip()
            run.font.size = Pt(16)
            run.font.bold = True
            run.font.color.rgb = RGBColor(*colors["primary"])
            run2 = p.add_run()
            run2.text = f"\n{parts[1].strip()}"
            run2.font.size = Pt(13)
            run2.font.color.rgb = RGBColor(*colors["text"])
        else:
            p.text = item
            p.font.size = Pt(14)
            p.font.color.rgb = RGBColor(*colors["text"])
        p.space_after = Pt(14)

    # Right column
    right_items = [b.strip() for b in right_content.replace(";", "\n").split("\n") if b.strip()]
    txBox2 = slide.shapes.add_textbox(Inches(6.8), Inches(1.8), Inches(5.5), Inches(5))
    tf2 = txBox2.text_frame
    tf2.word_wrap = True
    for i, item in enumerate(right_items):
        p = tf2.paragraphs[0] if i == 0 else tf2.add_paragraph()
        parts = item.split("|", 1)
        if len(parts) == 2:
            run = p.add_run()
            run.text = parts[0].strip()
            run.font.size = Pt(16)
            run.font.bold = True
            run.font.color.rgb = RGBColor(*colors["primary"])
            run2 = p.add_run()
            run2.text = f"\n{parts[1].strip()}"
            run2.font.size = Pt(13)
            run2.font.color.rgb = RGBColor(*colors["text"])
        else:
            p.text = item
            p.font.size = Pt(14)
            p.font.color.rgb = RGBColor(*colors["text"])
        p.space_after = Pt(14)

    # Vertical divider
    div = slide.shapes.add_shape(1, Inches(6.5), Inches(1.8), Inches(0.02), Inches(4.5))
    div.fill.solid()
    div.fill.fore_color.rgb = RGBColor(*colors["accent"])
    div.line.fill.background()
    div.rotation = 0


