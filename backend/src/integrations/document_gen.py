async def create_docx(
    title: str,
    content: str,
    filename: str = "document.docx",
    session=None,
    workspace_id: str = "default",
) -> str:
    """Generate a professional Word document from markdown content."""
    Document, DocxPt, DocxRGBColor, WD_ALIGN_PARAGRAPH = _get_docx()
    doc = Document()
    style = doc.styles["Normal"]
    font = style.font
    font.name = "Calibri"
    font.size = DocxPt(11)
    font.color.rgb = DocxRGBColor(0x1a, 0x1a, 0x1a)

    # Title
    title_para = doc.add_heading(title, level=0)
    title_para.alignment = WD_ALIGN_PARAGRAPH.LEFT
    for run in title_para.runs:
        run.font.size = DocxPt(24)
        run.font.color.rgb = DocxRGBColor(0x1e, 0x40, 0xaf)

    # Parse markdown sections
    for line in content.split("\n"):
        stripped = line.strip()
        if not stripped:
            continue
        if stripped.startswith("## "):
            h = doc.add_heading(stripped[3:], level=1)
            for run in h.runs:
                run.font.color.rgb = DocxRGBColor(0x1e, 0x40, 0xaf)
        elif stripped.startswith("### "):
            h = doc.add_heading(stripped[4:], level=2)
            for run in h.runs:
                run.font.color.rgb = DocxRGBColor(0x33, 0x33, 0x33)
        elif stripped.startswith("- ") or stripped.startswith("* "):
            doc.add_paragraph(stripped[2:], style="List Bullet")
        elif stripped.startswith("1. "):
            doc.add_paragraph(stripped[3:], style="List Number")
        elif stripped == "---":
            doc.add_paragraph("_" * 50)
        else:
            para = doc.add_paragraph(stripped)
            para.space_after = DocxPt(6)

    # Footer
    section = doc.sections[0]
    footer = section.footer
    footer.paragraphs[0].text = "Generated by Raah"
    footer.paragraphs[0].alignment = WD_ALIGN_PARAGRAPH.CENTER

    safe_name = _safe_name(filename, ".docx")
    out_path = _outputs_dir() / safe_name
    doc.save(str(out_path))

    return json.dumps({
        "ok": True,
        "filename": safe_name,
        "path": str(out_path),
        "size_bytes": out_path.stat().st_size,
        "format": "docx",
    }, indent=2)


